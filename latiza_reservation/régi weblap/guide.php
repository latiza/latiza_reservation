                        <h2>Idegenvezetés</h2>
                        <br>
                        <p>2005 óta kísérek magyar csoportokat külföldi országokba. Jelenleg a Grósz Utazás és az 1000 Út Utazási Iroda útjain vezetek csoportokat.</p>

<p>Amikor megkérdezik, melyik úti célt ismerem a legjobban, nem tudok egyet sem kiemelni – mivel minden útra ugyanolyan alapossággal készülök fel.</p>

<p>Gyerekkoromban a Panoráma útikönyvek és a Világatlasz mellett nőttem fel. Apukámmal játszottuk, hogy felismerem-e az országok fővárosát, zászlaját és helyét a térképen. A családdal sátorral jártuk be Bulgáriát, Törökországot és Görögországot, az első repülős utam pedig 1987-ben Leningrádba vezetett. 18 évesen egy nyereményút Krétára és Szantorinire mutatta meg nekem, hogy létezik egy olyan hivatás, ahol egyszerre lehet tanulni, mesélni és utazni – az idegenvezetés.</p>

<p>Ez az álom évekkel később Egyiptomban vált valóra, ahol telepített idegenvezetőként kezdtem dolgozni. Azóta számos országban éltem és vezettem csoportokat, megismerve történelmüket, kultúrájukat és szokásaikat. Munkám során arra törekszem, hogy az utasok ne csak lássák, hanem meg is értsék az adott helyet, és minden utazásból maradandó, személyes élményekkel térjenek haza.</p>



